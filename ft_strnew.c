#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*ret;

	ret = NULL;
	if (!(ret = (char*)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	ft_bzero(ret, size + 1);
	return (ret);
}
